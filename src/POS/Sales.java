    private void txtPnameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPnameKeyReleased
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/maskeymartdb","root","");
            String pname = txtPname.getText();
            // Parse the product name to remove additional details
            
           pst = con.prepareStatement("SELECT product_id, product_name, product_price FROM products WHERE product_name LIKE ?");
           pst.setString(1, "%" + pname + "%");
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                
                 BigDecimal price = rs.getBigDecimal(3);
                txtPrice.setText(price.toString());
            }
            else{
             
                txtPrice.setText("");
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_txtPnameKeyReleased

