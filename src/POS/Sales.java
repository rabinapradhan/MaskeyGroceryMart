    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
    // Clean input by removing non-numeric characters
    String cashText = txtCash.getText().trim().replaceAll("[^\\d.]", "");
    String grandTotalText = txtGrandtotal.getText().trim().replaceAll("[^\\d.]", "");
    BigDecimal cashAmount = new BigDecimal(cashText);
    BigDecimal grandTotal = new BigDecimal(grandTotalText);

    BigDecimal returnAmount = cashAmount.subtract(grandTotal);
    txtReturn.setText(formatCurrency(returnAmount));
    receipt();
    }//GEN-LAST:event_btnPayActionPerformed

