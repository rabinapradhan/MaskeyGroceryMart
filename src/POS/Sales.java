    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
   
        String pname = txtPname.getText();
        BigDecimal price = new BigDecimal(txtPrice.getText());
        int qty = Integer.parseInt(txtQuantity.getText());
        BigDecimal total = price.multiply(BigDecimal.valueOf(qty));

        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.addRow(new Object[]{
            pname, 
            formatCurrency(price), // Format price as currency
            qty,
            formatCurrency(total) // Format total as currency
        });
         JOptionPane.showMessageDialog(null, "Cart Added Successfully");
        BigDecimal sum = BigDecimal.ZERO;
        for (int i = 0; i < model.getRowCount(); i++) {
            sum = sum.add(new BigDecimal(model.getValueAt(i, 3).toString().replaceAll("[^\\d.]", "")));
        }

        BigDecimal tax = BigDecimal.valueOf(0.1); // Assuming tax rate of 10%
        BigDecimal taxAmount = sum.multiply(tax);
        BigDecimal grandTotal = sum.add(taxAmount);

        txtSubtotal.setText(formatCurrency(sum));
        txtTax.setText(formatCurrency(taxAmount));
        txtGrandtotal.setText(formatCurrency(grandTotal));

        txtPname.setText("");
        txtPrice.setText("");
        txtQuantity.setText("");
        txtPname.requestFocus();
    }//GEN-LAST:event_btnAddActionPerformed

