public void receipt() {
       try {
        BigDecimal total = new BigDecimal(txtSubtotal.getText().replaceAll("[^\\d.]", ""));
        BigDecimal totalwithTax = new BigDecimal(txtGrandtotal.getText().replaceAll("[^\\d.]", ""));
        BigDecimal tax = new BigDecimal(txtTax.getText().replaceAll("[^\\d.]", ""));
        BigDecimal cashAmount = new BigDecimal(txtCash.getText().replaceAll("[^\\d.]", ""));
        BigDecimal returnAmount = new BigDecimal(txtReturn.getText().replaceAll("[^\\d.]", ""));
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        txtReceipt.setText("");
        txtReceipt.append("======================================================================\n");
        txtReceipt.append("                            DETAILED RECEIPT                 \n");
        txtReceipt.append("======================================================================\n");
        txtReceipt.append("Item\tQty\tUnit Price\tTotal\n");       
        for (int i = 0; i < model.getRowCount(); i++) {  // Loop through each row in the table model
            String pname = (String) model.getValueAt(i, 0); 
            BigDecimal price = new BigDecimal(model.getValueAt(i, 1).toString().replaceAll("[^\\d.]", "")); 
            int qty = (int) model.getValueAt(i, 2);
            BigDecimal amt = new BigDecimal(model.getValueAt(i, 3).toString().replaceAll("[^\\d.]", "")); 
            txtReceipt.append(String.format("%s\t%d\t%s\t%s\n", pname, qty, formatCurrency(price), formatCurrency(amt)));
           }        
        txtReceipt.append("----------------------------------------------------------------------------------------------------------------------------------------------------\n");
        txtReceipt.append(String.format("Subtotal:        %s\n", formatCurrency(total)));
        txtReceipt.append(String.format("Grand total amount:        %s\n", formatCurrency(totalwithTax)));
        txtReceipt.append(String.format("Tax:        %s\n", formatCurrency(tax)));
        txtReceipt.append(String.format("Cash Given:        %s\n", formatCurrency(cashAmount)));
        txtReceipt.append(String.format("Returned Change:        %s\n", formatCurrency(returnAmount)));
        txtReceipt.append("===============================================================\n");
         txtReceipt.append("                           THANK YOU, VISIT AGAIN!                \n");
        txtReceipt.append("===============================================================\n");
    } catch (Exception e) {
        txtReceipt.append("Error generating receipt: " + e.getMessage());
    }
    
}

    
